function acc(
  code: string,
  name: string,
  type: AccountType,
  subtype: string,
  isGroup: boolean,
  options?: {
    parent?: string;
    currency?: string;
    desc?: string;
    esName?: string;
    agriCategory?: AgriculturalCategory;
    depreciationRate?: number;
    taxDeductible?: boolean;
    contra?: boolean;
  }
): ChartAccount {
  return {
    code,
    name,
    name_translations: {
      en: name,
      es: options?.esName,
      fr: undefined,
      ar: undefined,
    },
    account_type: type,
    account_subtype: subtype,
    is_group: isGroup,
    is_active: true,
    parent_code: options?.parent,
    currency_code: options?.currency || 'USD',
    description: options?.desc,
    agricultural_category: options?.agriCategory,
    depreciation_rate: options?.depreciationRate,
    tax_deductible: options?.taxDeductible,
    contra_account: options?.contra,
  };
}

